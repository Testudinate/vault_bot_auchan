'use strict';
/**
 * guardrails.js — обвязка безопасности для ИИ-ответов
 *
 * Из семинара: "способ не дать модели бесконтрольно фантазировать
 * и заставить её работать внутри заданного процесса"
 *
 * Проверки:
 *  1. Запретные темы (зарплаты, личное, юр. советы)
 *  2. Числа без источника
 *  3. "Не знаю" вместо галлюцинаций
 */

// Запретные темы для цифрового двойника
const FORBIDDEN_TOPICS = [
  { re: /зарплат|оклад|компенсаци|премия|бонус|salary|зп\b/i,
    reason: 'зарплаты и компенсации' },
  { re: /уволен|увольня|сокращен|fired|layoff/i,
    reason: 'кадровые решения' },
  { re: /пароль|password|логин и пароль|credentials|секретный ключ|api[_\s]?key/i,
    reason: 'учётные данные' },
  { re: /личн(ая|ую|ой) жизн|развод|болезн|диагноз|медицин/i,
    reason: 'личная информация' },
];

// Темы требующие осторожности (юр/фин советы)
const CAUTION_TOPICS = [
  { re: /юридическ|правов|legal|суд|иск|претензи/i,
    note: 'Это вопрос требует консультации юриста.' },
  { re: /налог|бухгалт|инвестиц|финансов(ый|ая) совет/i,
    note: 'Это вопрос требует консультации финансового специалиста.' },
];

/**
 * Проверка ВХОДЯЩЕГО запроса (до обработки)
 * @returns {block: bool, reason: string, note: string}
 */
function checkInput(question) {
  // Запретные темы
  for (const t of FORBIDDEN_TOPICS) {
    if (t.re.test(question)) {
      return {
        block:  true,
        reason: t.reason,
        message: 'Извините, я не могу обсуждать ' + t.reason +
                 '. По этому вопросу обратитесь к Станиславу напрямую или в соответствующий отдел.',
      };
    }
  }

  // Темы с осторожностью — не блокируем, но добавляем дисклеймер
  for (const t of CAUTION_TOPICS) {
    if (t.re.test(question)) {
      return { block: false, note: t.note };
    }
  }

  return { block: false };
}

/**
 * Проверка ИСХОДЯЩЕГО ответа (после генерации)
 * Добавляет дисклеймеры если в ответе числа без источников
 */
function checkOutput(answer, hasSources) {
  let result = answer;

  // Если в ответе есть конкретные числа/суммы, но нет источников
  const hasNumbers = /\d{2,}[\s]*(?:руб|₽|долл|\$|€|тыс|млн|млрд|процент|%|штук|шт)/i.test(answer);
  const hasDates   = /\d{1,2}[\.\/]\d{1,2}[\.\/]\d{2,4}/.test(answer);

  if ((hasNumbers || hasDates) && !hasSources) {
    result += '\n\n⚠️ Числа и даты требуют проверки по первоисточнику.';
  }

  return result;
}

/**
 * Системный промпт с guardrails
 */
function getGuardrailSystem(basePrompt) {
  return basePrompt + '\n\nВАЖНЫЕ ПРАВИЛА:\n' +
    '1. Отвечай ТОЛЬКО на основе предоставленных документов.\n' +
    '2. Если информации нет в документах — скажи "В базе знаний нет точной информации по этому вопросу" вместо догадок.\n' +
    '3. Конкретные числа, суммы, даты приводи ТОЛЬКО если они есть в документах, со ссылкой на источник.\n' +
    '4. НЕ придумывай факты, имена, цифры которых нет в контексте.\n' +
    '5. Если вопрос неоднозначный — уточни что именно нужно.';
}

/**
 * Детектор галлюцинаций — простая эвристика
 * Проверяет не противоречит ли ответ "не знаю" наличию данных
 */
function looksLikeHallucination(answer, docsCount) {
  // Если документов не найдено, но ответ содержит конкретные факты
  if (docsCount === 0) {
    const hasSpecifics = /\d{2,}|конкретн|точн|согласно|по данным/i.test(answer);
    const admitsUnknown = /не наш|нет информации|не знаю|не могу найти|отсутству/i.test(answer);
    if (hasSpecifics && !admitsUnknown) {
      return true; // подозрение на галлюцинацию
    }
  }
  return false;
}

module.exports = {
  checkInput,
  checkOutput,
  getGuardrailSystem,
  looksLikeHallucination,
  FORBIDDEN_TOPICS,
  CAUTION_TOPICS,
};
